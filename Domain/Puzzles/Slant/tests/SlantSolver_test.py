import unittest

from Domain.Board.Grid import Grid
from Domain.Puzzles.Slant.SlantSolver import SlantSolver

_ = SlantSolver.empty


class SlantSolverTests(unittest.TestCase):
    def test_solve_simple(self):
        clues = [
            [1, _],
            [_, 1]
        ]
        grid = Grid(clues)
        solver = SlantSolver(grid)
        solution = solver.get_solution()

        self.assertIsNotNone(solution)
        self.assertEqual(1, solution.rows_number)
        self.assertEqual(1, solution.columns_number)
        self.assertEqual(solution[0][0], 1)

    def test_5x5_easy_829484(self):
        grid_clues = Grid([
            [1, 1, 1, _, 2, _],
            [_, _, 2, 2, _, _],
            [_, 2, 2, _, _, 1],
            [0, 3, 2, 3, _, 1],
            [_, _, _, _, 1, _],
            [_, 1, 2, _, _, _],
        ])

        expected_solution = Grid([
            [1, 1, 1, 0, 1],
            [1, 1, 1, 0, 1],
            [1, 1, 1, 0, 1],
            [0, 1, 1, 1, 1],
            [1, 1, 0, 1, 0]
        ])

        solver = SlantSolver(grid_clues)
        solution = solver.get_solution()
        self.assertEqual(expected_solution, solution)
        other_solution = solver.get_other_solution()
        self.assertEqual(Grid.empty(), other_solution)

